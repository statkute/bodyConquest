package main.com.bodyconquest.entities;

/**
 * The interface Spawnable.
 */
public interface Spawnable {

    /**
     * Gets sugar cost.
     *
     * @return the sugar cost
     */
    int getSugarCost();

    /**
     * Gets lipid cost.
     *
     * @return the lipid cost
     */
    int getLipidCost();

    /**
     * Gets protein cost.
     *
     * @return the protein cost
     */
    int getProteinCost();

    /**
     * Gets portrait location.
     *
     * @return the portrait location
     */
    String getPortraitLocation();
}
